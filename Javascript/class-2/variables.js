/*JavaScript has two main categories of data types: Primitive and Non-Primitive (or Reference). 
Primitive Data Types:
These represent single, immutable values and are not objects.
String: Represents textual data, enclosed in single or double quotes (e.g., "hello", 'world').
Number: Represents both integer and floating-point numbers (e.g., 10, 3.14).
Boolean: Represents a logical entity and can only be true or false.
Undefined: Indicates that a variable has been declared but has not yet been assigned a value.
Null: Represents the intentional absence of any object value. It is a primitive value, but typeof null returns "object".
Symbol: Introduced in ES6, Symbols are unique and immutable values, often used as object property keys to avoid naming collisions.
BigInt: Introduced in ES11, BigInt can represent integers of arbitrary precision, beyond the safe integer limit of the Number type.
Non-Primitive (Reference) Data Types:
These represent collections of values or more complex entities, and their values are mutable. They are all variations of the Object type.
Object:
The fundamental non-primitive data type. Objects are collections of key-value pairs (properties) and can contain various data types, including other objects, arrays, and functions.
Array:
A specialized type of object used to store ordered collections of values. Elements are accessed by their numerical index.
Function:
A block of code designed to perform a particular task. In JavaScript, functions are also considered objects.
*/